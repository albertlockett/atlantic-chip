package ca.albertlockett.atlanticchip.model;

public class Racer {

	private int place;
	private int bibNo;
	private String name;
	private String city;
	private String Prov;
	private String divPlace;
	private String div;
	private String gunTime;
	private String kmpace;
	
	public int getPlace() {
		return place;
	}
	
	public void setPlace(int place) {
		this.place = place;
	}
	
	public int getBibNo() {
		return bibNo;
	}
	
	public void setBibNo(int bibNo) {
		this.bibNo = bibNo;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getCity() {
		return city;
	}
	
	public void setCity(String city) {
		this.city = city;
	}
	
	public String getProv() {
		return Prov;
	}
	
	public void setProv(String prov) {
		Prov = prov;
	}
	
	public String getDivPlace() {
		return divPlace;
	}
	
	public void setDivPlace(String divPlace) {
		this.divPlace = divPlace;
	}
	
	public String getDiv() {
		return div;
	}
	
	public void setDiv(String div) {
		this.div = div;
	}
	
	public String getGunTime() {
		return gunTime;
	}
	
	public void setGunTime(String gunTime) {
		this.gunTime = gunTime;
	}
	
	public String getKmpace() {
		return kmpace;
	}
	
	public void setKmpace(String kmpace) {
		this.kmpace = kmpace;
	}
	
}
