create table RACE (
  RACE_ID       NUMBER(9, 0),
  RACE_NAME      VARCHAR2(200) NULL,
  RACE_LOCATION  VARCHAR2(200) NULL,
  RACE_TYPE NUMBER(2, 0)  NULL,
  DISTANCE NUMBER(5, 0) NULL,
  PRIMARY KEY (RACE_ID)
);

CREATE TABLE RACER (
  RACER_ID    NUMBER(9, 0),
  RACE_ID     NUMBER(9, 0),
  PLACE       VARCHAR2(50),
  BIB_NO      VARCHAR2(50),
  RACER_NAME  VARCHAR2(50),
  CITY        VARCHAR2(50),
  PROV        VARCHAR2(50),
  COUNTRY     VARCHAR2(50),
  GENDER      VARCHAR2(50),
  DIV_PLACE   VARCHAR2(50),
  DIV         VARCHAR2(50),
  GUN_TIME    VARCHAR2(50),
  CHIP_TIME   VARCHAR2(50),
  KM_PACE     VARCHAR2(50),
  MILE_PACE   VARCHAR2(50),
  PRIMARY KEY (RACER_ID)
);

create sequence seq_racer_id increment by 1 maxvalue 9999999999999999999;